function [zreci] = rlvlh2reci(t, zlvlh, zeci_t, mu)
% Change of coordinates: from LVLH to RECI coordinates.

%--------------------------------------------------------------------------
% Rotation matrix
%--------------------------------------------------------------------------
Cs = lvlh2eciRotMat(zeci_t, t, mu);

%--------------------------------------------------------------------------
% COC
%--------------------------------------------------------------------------
zreci = Cs*zlvlh;

end
