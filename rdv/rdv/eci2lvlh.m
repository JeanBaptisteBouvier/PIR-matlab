function [zlvlh] = eci2lvlh(t, zeci, zeci_t, mu)
% Change of coordinates: from ECI to LVLH  coordinates.

%--------------------------------------------------------------------------
% Rotation matrix
%--------------------------------------------------------------------------
Cs = lvlh2eciRotMat(zeci_t, t, mu);

%--------------------------------------------------------------------------
% COC
%--------------------------------------------------------------------------
zlvlh = Cs\(zeci - zeci_t);

end
